fruits=('apple','banana','mango','grapes','orange')
#slice from index 1 to 3(does not include index 3)
print(fruits[1:3])
#slice from begining up to inner 3
print(fruits[:3])
#slice from index 2 to end
print(fruits[2:])
#step slicing: every secon element
print(fruits[::2])
#reverse tuple
print(fruits[::-1])